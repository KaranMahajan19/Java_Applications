interface A
{
	//abstract method
	public abstract void m1();
}

class Interface2
{
	public static void main(String[] args)
	{
		//Anonnymous inner class
		A a = new A()
		{
			public void m1()
			{
				System.out.println("From M1 mehtod");
			}
		};
		a.m1();
	}
}

