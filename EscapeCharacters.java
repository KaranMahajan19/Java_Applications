// class Name
class EscapeCharacters
{
	//main method
	public static void main(String[] args)
	{
		System.out.println("---------------------------------");
		System.out.println("New Line : \nKaran\nMahajan");
		System.out.println("---------------------------------");
		System.out.println("Horizontal Tab : \nKaran\tMahajan");
		System.out.println("---------------------------------");
		System.out.println("Back Space : \nKaran\bMahajan");
		System.out.println("---------------------------------");
		System.out.println("Carriage return : \nKaran\rMahajan");
		System.out.println("---------------------------------");
		System.out.println("Form feeding : \nKaran\fMahajan");
		System.out.println("---------------------------------");
		System.out.println("Back Slash : \nKaran\\Mahajan");
		System.out.println("---------------------------------");
		System.out.println("Double Quote : \nKaran\"Mahajan\"");
		System.out.println("---------------------------------");
		System.out.println("Single Quote : \nKaran\'Mahajan\'");
		System.out.println("---------------------------------");
	}
}