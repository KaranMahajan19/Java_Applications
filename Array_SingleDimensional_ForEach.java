//
//	Write a java program to display elements for each loop
//
///////////////////////////////////////////////////////////////////


class Array_SingleDimensional_ForEach
{
	public static void main(String[] args)
	{
		int arr[] = {10,20,30,40};

		//inserting elements

		//for each loop
		for(int i:arr)
		{
			System.out.print(i+" ");
		}
	}
}


