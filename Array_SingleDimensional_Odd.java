//
//	Write a java program to display odd elements form given array
//
/////////////////////////////////////////////////////////////////////


class Array_SingleDimensional_Odd
{
	public static void main(String[] args)
	{
		int arr[] = {4,6,5,9,8,4};

		// for each
		for(int i:arr)
		{
			if(i%2==1)
			{
				System.out.print(i+" ");
			}
		}
	}
}


