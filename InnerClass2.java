
/////////////////////////////////////////////////////////////////////////////////////
//	
//	Inner Classess : 
//
//	Declare a class inside another class
//
//	Accessing Inner class data from non static area of outer class
//
/////////////////////////////////////////////////////////////////////////////////////

// outer class
class InnerClass2
{
	//inner class
	class Inner
	{
		public void m1()
		{
			System.out.println("m1 method");
		}
	}

	public void m2()
	{
		Inner i = new Inner();
		i.m1();
	}

	public static void main(String[] args)
	{
		InnerClass2 i = new InnerClass2();
		i.m2();
	}

	
}
////////////////////////////////////////////////////////////////////////////////////
//
//	Output	:	m1 method
//
