//
//	Anonymous Array 	:	Nameless array
//
/////////////////////////////////////////////////////////////////////////////////////

class Array_AnonymousArray2
{
	public static int anonymousArray(int[] arr)	// no return with argument
	{
		int sum=0;
		for(int i:arr)
		{
			sum += i;
		}
		return sum;
	}

	//main method
	public static void main(String[] args)
	{
		//caller method
		System.out.println(anonymousArray(new int[]{10,20,30}));
	}
}

