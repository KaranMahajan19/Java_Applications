class Array_SingleDimensionalFor
{
	public static void main(String[] args)
	{
		int arr[] = {10,20,30,40};

		//inserting elements

		//for loop
		for(int i=0; i<arr.length; i++)
		{
			System.out.print(arr[i]+" ");
		}
	}
}


