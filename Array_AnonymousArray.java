/////////////////////////////////////////////////////////////////////////////////////
//
//	Anonymous Array 	:	Nameless array
//
/////////////////////////////////////////////////////////////////////////////////////

class Array_AnonymousArray
{
	public static void anonymousArray(int[] arr)	// no return with argument
	{
		int sum=0;
		for(int i:arr)
		{
			sum += i;
		}
		System.out.println(sum);
	}

	//main method
	public static void main(String[] args)
	{
		//caller method
		anonymousArray(new int[]{10,20,30});
	}
}

